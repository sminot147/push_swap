char	*ft_strrchr(const char *s, int c)
{
	char	*ptn;

	ptn = 0;
	s--;
	while (*++s)
		if (*s == (char)c)
			ptn = (char *)s;
	if ((char)c == '\0')
		return ((char *)s);
	return (ptn);
}
